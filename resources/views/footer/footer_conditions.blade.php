    <p> <h3>O kolačićima</h3>
<br>
<br>
  Kolačić je mala datoteka koju preglednik prima i pohranjuje na vaš uređaj svaki put kada posjetite određenu web stranicu (poput cuspajz.hr).
<br>
<br>
  Kolačići su potrebni kako bi web mjesto bilo brže, učinkovitije i kako bi administratorima pružilo detaljnije informacije o posjetiteljima, koje će se koristiti za prikupljanje statističkih podataka i poboljšanje performansi portala. Prvo, prilagodite navigaciju svakom posjetitelju (npr. Prepoznajući ga prilikom povezivanja s računom, ne prikazivanjem irelevantnih oglasa itd.). Ovisno o vrsti, kolačići mogu biti valjani od jedne sesije do nekoliko godina.
<br>
<br>
    <p> <h3>Koje kolačiće koristimo i u koje svrhe?</h3>
<br>
<br>
  Na našoj web stranici koristimo različite vrste kolačića koji se prema funkcijama mogu podijeliti u nekoliko skupina::
  <br>
  · Navigacijski. Najsuvremeniji i najjasniji kolačići za korisnika. Potrebne su za poboljšanje kvalitete web mjesta i svih njegovih značajki, kao što je pristup područjima web mjesta koja su nevidljiva drugim korisnicima.
  <br>
  · Analitički. Ova vrsta kolačića koristi se za statističku analizu posjetitelja naših web stranica. Na temelju njega možemo odlučiti koji je sadržaj najrelevantniji i najpopularniji te kako posjetitelji o njemu najčešće uče. Dobiveni rezultati potpuno su anonimni, dobivaju se upotrebom trećih usluga koje implementiraju vlastite kolačiće.
  <br>
  · Ciljano. Čini se da se ti kolačići povezuju na više web stranica koje je korisnik posjetio. Na primjer, nakon što pogledate proizvod na jednoj web lokaciji, na drugom ćete mjestu možda vidjeti bilbord (retargeting). Ove kolačiće nam šalju pouzdane tvrtke trećih strana. Nemamo pristup informacijama o kolačićima drugih web stranica, to je u potpunosti pod njihovom kontrolom.
  <br>
  · Funkcionalni. Funkcionalni kolačići koriste se, na primjer, za memoriranje korisničkih postavki. Nisu potrebni, ali značajno poboljšavaju korisničko iskustvo i kvalitetu navigacije.
<br>
